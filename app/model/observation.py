def create_empty_observation() -> dict:
    """
    Create an empty Canonical Observation object.

    Purpose
    -------
    Creates a new Observation instance following the
    Canonical Observation Framework.

    Every call creates new mutable dictionaries and lists.

    Observation instances therefore do not share mutable
    state.

    Returns
    -------
    dict
        Empty Canonical Observation object.

    Example
    -------
    observation = create_empty_observation()

    observation["identity"]["measurement_id"] = 186325158

    observation["identity"]["probe_id"] = 1003350

    observation["metadata"]["timestamp"] = 1782991276

    observation["measurement"]["dns"]["question"][
        "query_name"
    ] = "2.pool.ntp.org."

    observation["network"]["response_time_ms"] = 46.505

    observation["protocol"]["dns"]["packet"][
        "raw_abuf"
    ] = "QG6FAAABAAQAAA..."
    """

    return {

        "identity": {},

        "metadata": {},

        "measurement": {

            "definition": {},

            "schedule": {},

            "network": {},

            "dns": {
                "question": {},
            },

            "target": {},

            "probes": {},

            "status": {},

            "options": {},
        },

        "probe": {

            "definition": {},

            "lifecycle": {},

            "status": {},
        },

        "network": {},

        "location": {},

        "protocol": {

            "name": None,

            "display_name": None,

            "family": None,

            "version": None,

            "decoded": {},
        },

        "routing": {

            "bgp": {

                "source": {},

                "destination": {},

                "path_attributes": {},
            },
        },

        "path": {

            "traceroute": {},
        },

        "status": {},

        "telemetry": {},
    }