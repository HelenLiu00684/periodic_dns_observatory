def create_empty_observation():
    """
    Create an empty Observation object.

    Purpose
    -------
    Creates a new Observation instance following the
    Canonical Observation Framework.

    This function guarantees that every observation
    contains all required sections before being populated
    by the Normalizer.

    Returns
    -------
    dict
        Empty Observation object.

    Example
    -------
    observation = create_empty_observation()

    observation["metadata"]["timestamp"] = 1781482173
    observation["network"]["resolver_ip"] = "4.2.2.1"
    """

    return {

        "identity": {},

        "metadata": {},

        "network": {},

        "address_family": {},

        "location": {},

        "protocol": {},

        "routing": {},

        "path": {},

        "telemetry": {}

    }