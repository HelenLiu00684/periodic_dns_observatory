#!/bin/bash

# ==========================================================
# Project : DNS Measurement Platform
# Script  : vacuum.sh
#
# Description
# -----------
# Rebuild and optimize the SQLite database.
#
# Usage
# -----
# ./tools/db/vacuum.sh
#
# Author
# ------
# Helen Liu
# ==========================================================

# ==========================================================
# Configuration
# ==========================================================

DATABASE="data/dns_measurement.db"

# ==========================================================
# Header
# ==========================================================

echo "======================================================"
echo "DNS Measurement Platform"
echo "Database Vacuum"
echo "======================================================"

# ==========================================================
# Validation
# ==========================================================

if [ ! -f "$DATABASE" ]; then

    echo
    echo "Database"
    echo "------------------------------------------------------"
    echo "Database not found."

    echo
    echo "Status"
    echo "------------------------------------------------------"
    echo "FAIL"

    echo
    echo "======================================================"

    exit 1

fi

# ==========================================================
# Database Size Before
# ==========================================================

SIZE_BEFORE=$(du -h "$DATABASE" | cut -f1)

echo
echo "Database Size (Before)"
echo "------------------------------------------------------"
echo "$SIZE_BEFORE"

# ==========================================================
# Vacuum
# ==========================================================

echo
echo "Running VACUUM..."
echo

sqlite3 "$DATABASE" "VACUUM;"

# ==========================================================
# Database Size After
# ==========================================================

SIZE_AFTER=$(du -h "$DATABASE" | cut -f1)

echo
echo "Database Size (After)"
echo "------------------------------------------------------"
echo "$SIZE_AFTER"

echo
echo "Status"
echo "------------------------------------------------------"
echo "PASS"

echo
echo "======================================================"

exit 0