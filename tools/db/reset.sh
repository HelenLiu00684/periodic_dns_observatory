#!/bin/bash

# ==========================================================
# Project : DNS Measurement Platform
# Script  : reset.sh
#
# Description
# -----------
# Reset the SQLite database safely.
#
# Usage
# -----
# ./tools/db/reset.sh
#
# Author
# ------
# Helen Liu
# ==========================================================

# ==========================================================
# Configuration
# ==========================================================

DATABASE="data/dns_measurement.db"

# ==========================================================
# Header
# ==========================================================

echo "======================================================"
echo "DNS Measurement Platform"
echo "Database Reset"
echo "======================================================"

# ==========================================================
# Backup Database
# ==========================================================

echo
echo "Step 1 : Backup Database"
echo "------------------------------------------------------"

./tools/db/backup.sh

if [ $? -ne 0 ]; then

    echo
    echo "Backup failed."

    exit 1

fi

# ==========================================================
# Stop Collector
# ==========================================================

echo
echo "Step 2 : Stop Collector"
echo "------------------------------------------------------"

./tools/collector/stop.sh

# ==========================================================
# Delete Database
# ==========================================================

echo
echo "Step 3 : Remove Database"
echo "------------------------------------------------------"

rm -f "$DATABASE"

# ==========================================================
# Start Collector
# ==========================================================

echo
echo "Step 4 : Start Collector"
echo "------------------------------------------------------"

./tools/collector/start.sh

if [ $? -ne 0 ]; then

    echo
    echo "Collector failed to start."

    exit 1

fi

# ==========================================================
# Wait Database Creation
# ==========================================================

echo
echo "Waiting for collector..."

sleep 5

# ==========================================================
# Verification
# ==========================================================

echo
echo "Step 5 : Verify Database"
echo "------------------------------------------------------"

if [ -f "$DATABASE" ]; then

    echo "PASS"

else

    echo "FAIL"

    exit 1

fi

echo
echo "======================================================"
echo "Database reset completed successfully."
echo "======================================================"

exit 0